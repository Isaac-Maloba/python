# Python Data Types
# String - It is a series of characters that are inside of quotes
name = "Isaac Maloba"
print(name)
print(type(name))

# Numeric
#Int - This indicates a whole number
age = 39
print("The age of a person is: ", 39)
print(type(age))

# Float -  This is a number with a decimal
coordinates = 15.46479
print("The coordinates are: ", coordinates)
print(type(coordinates))