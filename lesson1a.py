# What is a variable: They are labels which act as containers to hold values. n python, a variable is always associated with some values.
# We use the assignment (=)operator to assign values to a variable
# Anything that is inside of quotes in python is called a string
message = "Hello there! Hope everything is fine"
print(message)

message2 = " good bye!"
print(message2)

#concatenating variables (Joining tow or more outputs together)

message3 = message + message2
print(message3)

# Variables can be named using a letter, number and underscore. They can start with a letter and an underscore but not a number

_name1 = "Brenda"
print(_name1)

# Variable names cannot contain spaces. We can use the underscore to separate the names

total_marks = 85
print(total_marks)

